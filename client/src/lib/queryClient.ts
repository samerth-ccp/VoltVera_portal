import { QueryClient, QueryFunction } from "@tanstack/react-query";

async function throwIfResNotOk(res: Response) {
  if (!res.ok) {
    const text = (await res.text()) || res.statusText;
    throw new Error(`${res.status}: ${text}`);
  }
}

export async function apiRequest(
  method: string,
  url: string,
  data?: unknown | undefined,
): Promise<Response> {
  const headers: Record<string, string> = data ? { "Content-Type": "application/json" } : {};
  const impersonationToken = typeof window !== 'undefined' ? sessionStorage.getItem('impersonationToken') : null;
  if (impersonationToken) {
    headers['Authorization'] = `Bearer ${impersonationToken}`;
  }

  const res = await fetch(url, {
    method,
    headers,
    body: data ? JSON.stringify(data) : undefined,
    credentials: "include",
  });

  await throwIfResNotOk(res);
  return res;
}

type UnauthorizedBehavior = "returnNull" | "throw";
export const getQueryFn: <T>(options: {
  on401: UnauthorizedBehavior;
}) => QueryFunction<T> =
  ({ on401: unauthorizedBehavior }) =>
  async ({ queryKey }) => {
    let url = queryKey[0] as string;
    
    // Handle search parameters for user queries
    if (queryKey.length > 1 && queryKey[0] === '/api/users') {
      const searchParam = queryKey[1] as string;
      if (searchParam) {
        url += `?search=${encodeURIComponent(searchParam)}`;
      }
    }
    
    const headers: Record<string, string> = {};
    const impersonationToken = typeof window !== 'undefined' ? sessionStorage.getItem('impersonationToken') : null;
    if (impersonationToken) {
      headers['Authorization'] = `Bearer ${impersonationToken}`;
    }

    const res = await fetch(url, {
      credentials: "include",
      headers,
    });

    if (unauthorizedBehavior === "returnNull" && res.status === 401) {
      return null;
    }

    await throwIfResNotOk(res);
    return await res.json();
  };

export const queryClient = new QueryClient({
  defaultOptions: {
    queries: {
      queryFn: getQueryFn({ on401: "throw" }),
      refetchInterval: false,
      refetchOnWindowFocus: false,
      staleTime: Infinity,
      retry: false,
    },
    mutations: {
      retry: false,
    },
  },
});
